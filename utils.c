/* ============================================================
 *  clear_input
 *  功能: 清空标准输入（stdin）缓冲区中的残留字符
 *  使用场景:
 *    - 调用 scanf 读取整数/浮点数后，\n 仍留在缓冲区
 *      下次调用 fgets 会直接读到空行，需先清空
 *    - 读取错误输入后重置缓冲区
 *  实现: 循环读取字符直到遇到换行符或 EOF 为止
 * ============================================================ */
void clear_input(void) {
    int c;
    /* getchar() 每次读取一个字符；\n 表示行结束，EOF 表示流结束 */
    while ((c = getchar()) != '\n' && c != EOF) {}
}

/* ============================================================
 *  now_time_str
 *  功能: 获取当前本地时间，格式化为字符串写入 out 缓冲区
 *  参数: out —— 长度至少为 TIME_LEN（24）的字符数组
 *  输出格式: "yyyy-mm-dd hh:mm:ss"（19个字符 + 终止符）
 *  示例: "2025-03-20 14:30:00"
 * ============================================================ */
void now_time_str(char out[TIME_LEN]) {
    time_t t = time(NULL);           /* 获取当前 Unix 时间戳（秒数）*/
    struct tm *lt = localtime(&t);   /* 转换为本地时间结构体 */

    /* sprintf 格式说明:
     *   %04d → 年份，不足4位补零
     *   %02d → 月/日/时/分/秒，不足2位补零
     */
    sprintf(out, "%04d-%02d-%02d %02d:%02d:%02d",
            lt->tm_year + 1900,  /* tm_year 是从1900年起的偏移量 */
            lt->tm_mon  + 1,     /* tm_mon  范围 0-11，需加1 */
            lt->tm_mday,
            lt->tm_hour,
            lt->tm_min,
            lt->tm_sec);
}

/* ============================================================
 *  status_str
 *  功能: 将 StationStatus 枚举值转换为对应的中文字符串
 *  返回: 静态字符串常量指针（不需要也不能 free）
 *  用途: 在界面打印充电桩状态时调用
 * ============================================================ */
const char *status_str(StationStatus s) {
    switch (s) {
        case STATUS_IDLE:     return "空闲  ";   /* 补空格，保持列对齐 */
        case STATUS_CHARGING: return "充电中";   /* 3个中文字符 = 6字节 */
        case STATUS_FAULT:    return "故障  ";
        default:              return "未知  ";
    }
}

/* ============================================================
 *  find_station_idx
 *  功能: 在 g_stations[] 数组中按 ID 线性查找充电桩
 *  参数: id —— 要查找的充电桩唯一编号
 *  返回: 找到时返回数组下标（0 起始）；未找到返回 -1
 *  时间复杂度: O(n)，n 为当前充电桩数量
 *  调用者应检查返回值是否为 -1 再使用下标
 * ============================================================ */
int find_station_idx(int id) {
    for (int i = 0; i < g_station_count; i++) {
        if (g_stations[i].id == id)
            return i;  /* 找到，立即返回下标 */
    }
    return -1;  /* 遍历结束未找到 */
}

/* ============================================================
 *  find_active_record_by_station
 *  功能: 查找某个充电桩当前「进行中」的充电记录
 *  参数: station_id —— 充电桩编号
 *  返回: 找到时返回记录在 g_records[] 中的下标；未找到返回 -1
 *  说明: finished==0 表示充电会话进行中
 *        每个充电桩同一时刻最多有一条进行中记录
 * ============================================================ */
int find_active_record_by_station(int station_id) {
    for (int i = 0; i < g_record_count; i++) {
        if (g_records[i].station_id == station_id
            && g_records[i].finished == 0) {
            return i;
        }
    }
    return -1;
}

/* ============================================================
 *  press_any_key
 *  功能: 提示用户按 Enter 键，然后等待输入
 *  用途: 在显示信息后暂停，让用户有时间阅读再返回菜单
 * ============================================================ */
void press_any_key(void) {
    printf("\n按 Enter 键继续...");
    /* clear_input 会阻塞直到读到 \n，实现「等待按键」效果 */
    clear_input();
}

/* ============================================================
 *  print_title
 *  功能: 打印带等号边框的标题栏，用于区分不同功能页面
 *  参数: title —— 要显示的标题文本
 *  示例输出:
 *    ============================================
 *                  充电桩管理
 *    ============================================
 * ============================================================ */
void print_title(const char *title) {
    /* 边框总宽度（字节数，含中文字符每个占2-3字节）*/
    int total = 48;

    printf("\n");
    /* 打印上边框 */
    for (int i = 0; i < total; i++) printf("=");
    printf("\n");

    /* 计算标题前的空格数（简单居中，英文环境下准确，中文可能略有偏差）*/
    int len = (int)strlen(title);
    int pad = (total - len) / 2;
    if (pad < 0) pad = 0;
    for (int i = 0; i < pad; i++) printf(" ");
    printf("%s\n", title);

    /* 打印下边框 */
    for (int i = 0; i < total; i++) printf("=");
    printf("\n");
}

